import { test, expect } from "vitest";

import { Note } from "@/classes/Note";
import { NaturalNote } from "@/utilities/naturalNote";
import { SolfegeLetter } from "@/utilities/solfege";
import { DEFAULT_SETTINGS } from "@/utilities/settings";


test("Note works", () => {
  const notClusteredNotesSettings = { ...DEFAULT_SETTINGS, isClusteringNotes: false };
  const clusteredNotesSettings = { ...DEFAULT_SETTINGS, isClusteringNotes: true };

  const note1 = new Note(0, 0);
  expect(
    note1.value
  ).toBe(
    0
  );
  expect(
    note1.naturalNote
  ).toBe(
    NaturalNote.D
  );
  expect(
    note1.sharpsCount
  ).toBe(
    0
  );
  expect(
    note1.name
  ).toBe(
    "D"
  );
  expect(
    note1.solfegeLetter
  ).toBe(
    SolfegeLetter.Do
  );
  expect(
    note1.position
  ).toBe(
    0
  );
  expect(
    note1.getHour(notClusteredNotesSettings)
  ).toBe(
    0
  );
  expect(
    note1.getHour(clusteredNotesSettings)
  ).toBe(
    0
  );

  const note2 = new Note(0, -2);
  expect(
    note2.value
  ).toBe(
    -2
  );
  expect(
    note2.naturalNote
  ).toBe(
    NaturalNote.C
  );
  expect(
    note2.sharpsCount
  ).toBe(
    0
  );
  expect(
    note2.name
  ).toBe(
    "C"
  );
  expect(
    note2.solfegeLetter
  ).toBe(
    SolfegeLetter.Ti
  );
  expect(
    note2.position
  ).toBe(
    -2
  );
  expect(
    note2.getHour(notClusteredNotesSettings)
  ).toBe(
    10
  );
  expect(
    note2.getHour(clusteredNotesSettings)
  ).toBe(
    10
  );

  const note3 = new Note(-4, 0);
  expect(
    note3.value
  ).toBe(
    -4
  );
  expect(
    note3.naturalNote
  ).toBe(
    NaturalNote.B
  );
  expect(
    note3.sharpsCount
  ).toBe(
    -1
  );
  expect(
    note3.name
  ).toBe(
    "B♭"
  );
  expect(
    note3.solfegeLetter
  ).toBe(
    SolfegeLetter.Do
  );
  expect(
    note3.position
  ).toBe(
    0
  );
  expect(
    note3.getHour(notClusteredNotesSettings)
  ).toBe(
    8
  );
  expect(
    note3.getHour(clusteredNotesSettings)
  ).toBe(
    8
  );

  const note4 = new Note(5, 2);
  expect(
    note4.value
  ).toBe(
    7
  );
  expect(
    note4.naturalNote
  ).toBe(
    NaturalNote.D
  );
  expect(
    note4.sharpsCount
  ).toBe(
    1
  );
  expect(
    note4.name
  ).toBe(
    "D♯"
  );
  expect(
    note4.solfegeLetter
  ).toBe(
    SolfegeLetter.Re
  );
  expect(
    note4.position
  ).toBe(
    2
  );
  expect(
    note4.getHour(notClusteredNotesSettings)
  ).toBe(
    1
  );
  expect(
    note4.getHour(clusteredNotesSettings)
  ).toBe(
    7
  );

  const note5 = new Note(7, -3);
  expect(
    note5.value
  ).toBe(
    4
  );
  expect(
    note5.naturalNote
  ).toBe(
    NaturalNote.F
  );
  expect(
    note5.sharpsCount
  ).toBe(
    1
  );
  expect(
    note5.name
  ).toBe(
    "F♯"
  );
  expect(
    note5.solfegeLetter
  ).toBe(
    SolfegeLetter.Mi
  );
  expect(
    note5.position
  ).toBe(
    -3
  );
  expect(
    note5.getHour(notClusteredNotesSettings)
  ).toBe(
    4
  );
  expect(
    note5.getHour(clusteredNotesSettings)
  ).toBe(
    4
  );
});
